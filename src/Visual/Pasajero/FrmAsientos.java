    private void seatListener() {
        ArrayList<String> list = ComprarBoletoManager.getInstace().getListSeat(bus);
        for (Component i : contentSeats.getComponents()) {
            JButton button = (JButton) i;
            if (list.contains(button.getText())) {
                button.setBackground(Color.RED);
            }
            if(seats.contains(button.getText())){
                button.setBackground(Color.GREEN);
            }
            button.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    if (button.getBackground().equals(Color.RED)) {
                        return;
                    }
                    if (ComprarBoletoManager.getInstace().getAsientos().contains(button.getText())) {
                        button.setBackground(Color.getColor("Gray94"));
                        ComprarBoletoManager.getInstace().getAsientos().remove(button.getText());
                        return;
                    }
                    System.out.println(numAsientos);
                    if (seats.size() >= numAsientos) {
                        JOptionPane.showMessageDialog(null, "Â¡Haz alcanzado el limite!");
                        return;
                    }
                    ComprarBoletoManager.getInstace().putAsiento(button.getText());
                    button.setBackground(Color.GREEN);
                }
            });
        }
    }

